#include <stdio.h>

int main() {
    int num1 = 15;
    int num2 = 4;
    int sum = num1 + num2;
    int difference = num1 - num2;
    int product = num1 * num2;
    int quotient = num1 / num2;
    
    printf("Sum: %i\n", sum);
    printf("Difference: %i\n", difference);
    printf("Product: %i\n", product);
    printf("Quotient: %i\n", quotient);
    return 0;
}